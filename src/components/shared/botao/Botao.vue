<template>
    <button class="botao botao-perigo" :type="tipo" @click="disparaAcao()">{{rotulo}}</button>
</template>


<script>
export default{
    props: ['tipo', 'rotulo', 'confirmacao'],
    methods: {

       disparaAcao() {
           if(this.confirmacao){
                if(confirm('Confirma operacao?')) {
                    this.$emit('botaoAtivado');
                }
                return;
            }
            this.$emit('botaoAtivado');
        }
    }
}
</script>

